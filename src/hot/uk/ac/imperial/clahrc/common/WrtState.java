@Name("wrtState")
@Scope(ScopeType.APPLICATION)
@AutoCreate
@Synchronized
public class WrtState {

	private boolean suspended = false;
	
	public boolean isSuspended() {
		return suspended;
	}

	public void setSuspended(boolean suspended) {
		this.suspended = suspended;
	}

}
